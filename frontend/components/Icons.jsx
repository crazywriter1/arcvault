// Token icon badges — colored circles with symbol
export function TokenBadge({ symbol, size = 32 }) {
  const colors = {
    USDC: { bg: '#2775CA', fg: '#fff' },
    EURC: { bg: '#0052B4', fg: '#fff' },
    ETH: { bg: '#627EEA', fg: '#fff' },
    ARC: { bg: '#00d9ff', fg: '#07070b' },
  };
  const c = colors[symbol] || { bg: '#3a3a4a', fg: '#e8e8ef' };
  return (
    <div
      className="rounded-full flex items-center justify-center font-bold"
      style={{
        width: size,
        height: size,
        background: c.bg,
        color: c.fg,
        fontSize: size * 0.38,
      }}
    >
      {(symbol || '?').slice(0, 2)}
    </div>
  );
}
